L["FAQ_1_3"] = [=[
|cffffffff父級框體和錨點|r

kgPanels支持使用你定義的面板名稱當作父級框體。
不要使用以 kgPanels 開頭的名字作為父級框體名。這樣做可能會導致一些不可預知的問題，因為kgPanels會自動依據需要回收框體。
因此，假如你創建了個面板叫做“某面板”，你就可以使用“某面板”來當作父級框體以及錨點的名稱。
]=]
L["FAQ_1_4"] = [=[
|cffffffff獲取框體名|r

kgPanels的設置系統為框體獲取添加了一個快捷鍵。在鍵位設置裡為框體獲取器設置一個鍵位後，只需要按下這個鍵就會打開框體名獲取功能並顯示你鼠標指向的框體名。
]=]
