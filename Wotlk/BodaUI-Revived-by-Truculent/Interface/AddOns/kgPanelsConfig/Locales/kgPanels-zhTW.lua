L["FAQ_5_3"] = [=[
|cffffffff變量是如何傳遞的？|r

每個句柄都可以傳遞到高等級的參數（參照WOWWiki的Event API）。
第一個變量永遠是self。

OnEvent，我們把這個變量參數分離了，這樣你可以很簡單的引用arg1, arg2, arg3...等等。|r

OnUpdate提供了時間相關的參數屬性。|r

OnClick則是按鈕參數。|r

]=]
L["FAQ_5_4"] = [=[
|cffffffff我添加了一個OnEvent腳本但是它根本沒觸發過，少了什麼東西？|r

在你的OnLoad裡，為你想要獲取的事件注冊，例如：|r
self:RegisterEvent("PLAYER_LOGIN")|r

同時，非WoW創建的事件是不可用的，所以那些AceEvent-3.0的消息並不會觸發你的OnEvent。|r
不過你也可以把AceEvent-3.0嵌入到你的框體指向裡，要注意的是這可能會使你一次收到2條同樣的事件。|r
]=]
